// ==================== WALLET ADAPTER ====================

/**
 * Minimal Wallet adapter for Anchor provider.
 * Wraps a Keypair to satisfy AnchorProvider's wallet interface.
 */
class KeypairWallet {
  constructor(readonly payer: Keypair) {}

  get publicKey(): PublicKey {
    return this.payer.publicKey;
  }

  async signTransaction<T extends Transaction>(tx: T): Promise<T> {
    tx.partialSign(this.payer);
    return tx;
  }

  async signAllTransactions<T extends Transaction>(txs: T[]): Promise<T[]> {
    txs.forEach((tx) => tx.partialSign(this.payer));
    return txs;
  }
}

// ==================== CONNECTION & PROVIDER ====================

/**
 * Get or create a Solana RPC connection.
 * Defaults to devnet; override with SOLANA_RPC_URL env var.
 */
export function getConnection(): Connection {
  if (!_connection) {
    const rpcUrl =
      process.env.SOLANA_RPC_URL || "https://api.devnet.solana.com";
    _connection = new Connection(rpcUrl, "confirmed");
  }
  return _connection;
}

/**
 * Load the agent's wallet keypair from disk.
 *
 * Resolution precedence (highest first):
 *   1. `ANCHOR_WALLET`       — Anchor's documented convention. When set,
 *                              this wins outright. Honouring it lets the
 *                              MCP server inherit the wallet from
 *                              `anchor test`, `anchor run`, and any
 *                              IDE/CI integration that follows the
 *                              Anchor convention.
 *   2. `SOLANA_KEYPAIR_PATH` — legacy MCP-only env var. Kept for
 *                              backward compatibility with deployments
 *                              that already set it.
 *   3. `~/.config/solana/id.json` — Solana CLI default.
 *
 * AUD-016 (2026-04-25 audit): pre-fix, only `SOLANA_KEYPAIR_PATH` was
 * checked, so the standard Anchor env var was silently ignored and
 * users would see "wallet not found" errors despite a working Anchor
 * setup.
 *
 * ADR-083 Finding 5.1: refuses to load if the keyfile permission mode has any
 * group or other bits set (a `0644` keyfile is silently world-readable on
 * Unix). The check is `(mode & 0o077) === 0`. Same-uid attackers bypass this
 * trivially, but the silent-permissions-regression class of incidents
 * (backup-tool restore, misconfigured Ansible play) is caught at startup.
 */
export function loadWallet(): Keypair {
  if (_wallet) return _wallet;

  // AUD-016: ANCHOR_WALLET takes precedence over SOLANA_KEYPAIR_PATH so
  // the MCP server matches Anchor's documented convention. If both are
  // set, ANCHOR_WALLET wins (it's the more standard variable).
  let keypath = process.env.ANCHOR_WALLET || process.env.SOLANA_KEYPAIR_PATH;
  if (!keypath) {
    const home = process.env.HOME || process.env.USERPROFILE || "";
    keypath = path.join(home, ".config", "solana", "id.json");
  }

  if (!fs.existsSync(keypath)) {
    throw new Error(
      `Wallet keypair not found at: ${keypath}. ` +
        `Set ANCHOR_WALLET or SOLANA_KEYPAIR_PATH or run 'solana-keygen new'.`
    );
  }

  // ADR-083 Finding 5.1: refuse to load a world- or group-readable keyfile.
  assertKeyfilePermissions(keypath);

  const secretKey = JSON.parse(fs.readFileSync(keypath, "utf-8"));
  _wallet = Keypair.fromSecretKey(Buffer.from(secretKey));
  return _wallet;
}

/**
 * Get the wallet's public key (agent address).
 */
export function getWalletPublicKey(): PublicKey {
  return loadWallet().publicKey;
}

/**
 * Get or create an AnchorProvider bound to the agent's wallet.
 */
export function getProvider(): AnchorProvider {
  if (_provider) return _provider;
  const conn = getConnection();
  const kp = loadWallet();
  const wallet = new KeypairWallet(kp);
  // TODO(typed): Anchor's `Wallet` interface is declared in
  // @anchor-lang/core/dist/.../provider but the published `.d.ts` keeps the
  // interface internal, so a structurally-identical KeypairWallet doesn't
  // satisfy the public type. Unblocked by either (a) Anchor exporting
  // `Wallet`, or (b) ADR-099's `@agenomics/idl` package shipping its own
  // typed wrapper.
  _provider = new AnchorProvider(
    conn,
    wallet as unknown as AnchorProvider["wallet"],
    {
      commitment: "confirmed",
      preflightCommitment: "confirmed",
    }
  );
  return _provider;
}

// ==================== IDL LOADING & PROGRAMS ====================

/**
 * Load an Anchor IDL JSON file by program name.
 * IDL files are expected at ../../target/idl/<name>.json relative to dist/.
 *
 * Returns `unknown` because Anchor's `Program` constructor declares its
 * first argument as `idl: any` (see
 * `node_modules/@anchor-lang/core/dist/cjs/program/index.d.ts:238`). The
 * static type parameter on the constructed `Program<IDL>` instance is what
 * threads typing through every account / methods access — see ADR-088.
 */
function loadIdl(name: string): unknown {
  const idlPath = path.resolve(
    __dirname,
    "..",
    "..",
    "target",
    "idl",
    `${name}.json`
  );
  if (!fs.existsSync(idlPath)) {
    throw new Error(
      `IDL file not found: ${idlPath}. Run 'anchor build' to generate IDLs.`
    );
  }
  return JSON.parse(fs.readFileSync(idlPath, "utf-8"));
}

