// ==================== PDA DERIVATION ====================

/**
 * Derive vault PDA. Seeds: ["vault", authority]
 */
export function deriveVaultPDA(authority: PublicKey): [PublicKey, number] {
  return PublicKey.findProgramAddressSync(
    [Buffer.from("vault"), authority.toBuffer()],
    VAULT_PROGRAM_ID
  );
}

/**
 * ADR-097: Derive owner-nonce PDA. Seeds: [authority, "owner-nonce"].
 * Init'd by the first `register_agent`; its `nonce` feeds the agent_profile
 * PDA seed for Sybil resistance after close-then-reopen.
 */
export function deriveOwnerNoncePDA(
  authority: PublicKey
): [PublicKey, number] {
  return PublicKey.findProgramAddressSync(
    [authority.toBuffer(), Buffer.from("owner-nonce")],
    REGISTRY_PROGRAM_ID
  );
}

/**
 * ADR-097: Derive agent profile PDA. Seeds: [authority, "agent-profile", nonce-le].
 * Fresh authorities (first registration) use `nonce = 0n`. For authorities that
 * previously deregistered and are re-registering, pass the current owner_nonce.
 */
export function deriveAgentProfilePDA(
  authority: PublicKey,
  nonce: bigint = 0n
): [PublicKey, number] {
  const nonceBuf = Buffer.alloc(8);
  nonceBuf.writeBigUInt64LE(nonce);
  return PublicKey.findProgramAddressSync(
    [authority.toBuffer(), Buffer.from("agent-profile"), nonceBuf],
    REGISTRY_PROGRAM_ID
  );
}

/**
 * Derive escrow PDA. Seeds: ["escrow", client, provider, taskId_le_bytes]
 */
export function deriveEscrowPDA(
  client: PublicKey,
  provider: PublicKey,
  taskId: number
): [PublicKey, number] {
  const taskIdBuf = Buffer.alloc(8);
  taskIdBuf.writeBigUInt64LE(BigInt(taskId));
  return PublicKey.findProgramAddressSync(
    [
      Buffer.from("escrow"),
      client.toBuffer(),
      provider.toBuffer(),
      taskIdBuf,
    ],
    SETTLEMENT_PROGRAM_ID
  );
}

/**
 * Derive the escrow's associated token account (ATA).
 * Uses allowOwnerOffCurve=true since the escrow is a PDA.
 */
export function deriveEscrowTokenAccount(
  escrowPDA: PublicKey,
  tokenMint: PublicKey
): PublicKey {
  return getAssociatedTokenAddressSync(tokenMint, escrowPDA, true);
}

/**
 * Finding #19: Derive the singleton ProtocolConfig PDA.
 * Seeds: [b"protocol_config"] under the Settlement program.
 * Every escrow/dispute/approve/expire path now takes this account so the
 * governance-owned tunables (min_escrow, dispute_timeout, reputation
 * deltas) are read at runtime instead of baked into the binary.
 */
export function deriveProtocolConfigPDA(): [PublicKey, number] {
  return PublicKey.findProgramAddressSync(
    [Buffer.from("protocol_config")],
    SETTLEMENT_PROGRAM_ID
  );
}

// ==================== UTILITY FUNCTIONS ====================

// CC-5 / C4-MCPEVO-002: base58 alphabet + Solana pubkey length bound.
// `new PublicKey()` alone accepts any input it can coerce to 32 bytes
// (incl. 32-byte hex/array forms) — not a real base58-pubkey check. We
// pre-gate on the canonical base58 string shape (32–44 chars, base58
// alphabet) before constructing, so garbage strings are rejected up
// front rather than silently coerced.
const BASE58_PUBKEY_RE = /^[1-9A-HJ-NP-Za-km-z]{32,44}$/;

/**
 * Validate a base58-encoded Solana public key string. Real validation:
 * canonical base58 shape AND a 32-byte decode (`new PublicKey`). Pass
 * `requireOnCurve` to additionally reject off-curve points (e.g. when the
 * caller-supplied value must be a wallet/authority, not a PDA).
 */
export function isValidPublicKey(
  key: string,
  opts: { requireOnCurve?: boolean } = {},
): boolean {
  if (typeof key !== "string" || !BASE58_PUBKEY_RE.test(key)) {
    return false;
  }
  let pk: PublicKey;
  try {
    pk = new PublicKey(key);
  } catch {
    return false;
  }
  if (pk.toBase58() !== key) {
    return false;
  }
  if (opts.requireOnCurve && !PublicKey.isOnCurve(pk.toBuffer())) {
    return false;
  }
  return true;
}

/**
 * Parse a base58 string into a PublicKey, throwing on invalid input.
 */
export function parsePublicKey(key: string): PublicKey {
  if (!isValidPublicKey(key)) {
    throw new Error(`Invalid Solana public key: ${key}`);
  }
  return new PublicKey(key);
}

/**
 * Convert SOL to lamports.
 */
export function solToLamports(sol: number): number {
  return Math.floor(sol * LAMPORTS_PER_SOL);
}

/**
 * Convert lamports to SOL.
 */
export function lamportsToSol(lamports: number | bigint): number {
  return Number(lamports) / LAMPORTS_PER_SOL;
}

/**
 * Get the SOL balance of an address.
 */
export async function getBalance(pubkey: PublicKey): Promise<number> {
  const conn = getConnection();
  const balance = await conn.getBalance(pubkey);
  return balance / LAMPORTS_PER_SOL;
}

/**
 * SHA-256 hash a string and return as a 32-byte array (for description hashes).
 */
export function hashDescription(description: string): number[] {
  const hash = crypto.createHash("sha256").update(description).digest();
  return Array.from(hash);
}

